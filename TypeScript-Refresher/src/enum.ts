// Enums in TypeScript are a way to define a set of named constant values.

// often representing a collection of related options or choices.

enum Direction {
  north,
  east,
  west,
  south,
}

console.log(Direction);
console.log(Direction.north);
console.log(Direction[0]);

enum choice {
  option1 = "modi",
  option2 = "amit",
  option3 = "rahul",
  option4 = "kejri",
}

console.log(choice);
console.log(choice.option3);
